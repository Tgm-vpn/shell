/usr/local/x-ui/x-ui setting -username "$THREEXUI_USERNAME" -password "$THREEXUI_ROOT_PASSWORD" || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ –∏–º–µ–Ω–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –∏ –ø–∞—Ä–æ–ª—è."; exit 1; }
log_success "–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å '$THREEXUI_USERNAME' —Å –Ω–æ–≤—ã–º –ø–∞—Ä–æ–ª–µ–º —É—Å–ø–µ—à–Ω–æ –¥–æ–±–∞–≤–ª–µ–Ω/–æ–±–Ω–æ–≤–ª–µ–Ω. üîë"


