    log_error "–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: $0 <–ü–ê–†–û–õ–¨_–î–õ–Ø_3XUI_ROOT>"
    log_error "–ü—Ä–∏–º–µ—Ä: sudo ./install_3xui.sh My3XUIPassword123!"
    exit 1
