/usr/local/x-ui/x-ui setting -show || { log_error "–ù–µ —É–¥–∞–ª–æ—Å—å –æ—Ç–æ–±—Ä–∞–∑–∏—Ç—å —Ñ–∏–Ω–∞–ª—å–Ω—ã–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ 3x-ui."; }

log_info "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∏ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞ 3x-ui –∑–∞–≤–µ—Ä—à–µ–Ω—ã. ‚ú®"
