systemctl restart x-ui || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–µ 3x-ui —Å–µ—Ä–≤–∏—Å–∞."; exit 1; }
