#!/bin/bash

# –°–∫—Ä–∏–ø—Ç –¥–ª—è —É—Å—Ç–∞–Ω–æ–≤–∫–∏ –∏ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ 3x-ui –≤ Debian 12.
# –ü—Ä–µ–¥–ø–æ–ª–∞–≥–∞–µ—Ç—Å—è, —á—Ç–æ —Å–∫—Ä–∏–ø—Ç –∑–∞–ø—É—Å–∫–∞–µ—Ç—Å—è –æ—Ç –∏–º–µ–Ω–∏ root.
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./install_3xui.sh <–ü–ê–†–û–õ–¨_–î–õ–Ø_3XUI_ROOT>

# --- –ü–µ—Ä–µ–º–µ–Ω–Ω—ã–µ (–ó–ê–ú–ï–ù–ò–¢–ï –í–ê–®–ò –ó–ù–ê–ß–ï–ù–ò–Ø!) ---
# –ü–∞—Ä–æ–ª—å –¥–ª—è –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è 'root' –≤ 3x-ui –±—É–¥–µ—Ç –ø–µ—Ä–µ–¥–∞–Ω –∫–∞–∫ –ø–µ—Ä–≤—ã–π –∞—Ä–≥—É–º–µ–Ω—Ç —Å–∫—Ä–∏–ø—Ç–∞.
THREEXUI_ROOT_PASSWORD="$1"

# –ü–æ—Ä—Ç –∏ –∞–¥—Ä–µ—Å –¥–ª—è 3x-ui
THREEXUI_LISTEN_ADDRESS="127.0.0.1"
THREEXUI_LISTEN_PORT="10000"

# --- –§—É–Ω–∫—Ü–∏–∏ –¥–ª—è –ª–æ–≥–∏—Ä–æ–≤–∞–Ω–∏—è ---
log_info() {
    echo "INFO: $1"
}

log_error() {
    echo "ERROR: $1" >&2
}

log_success() {
    echo "SUCCESS: $1"
}

# --- –ü—Ä–æ–≤–µ—Ä–∫–∞ –∞—Ä–≥—É–º–µ–Ω—Ç–æ–≤ ---
if [ -z "$THREEXUI_ROOT_PASSWORD" ]; then
    log_error "–ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: $0 <–ü–ê–†–û–õ–¨_–î–õ–Ø_3XUI_ROOT>"
    log_error "–ü—Ä–∏–º–µ—Ä: sudo ./install_3xui.sh My3XUIPassword123!"
    exit 1
fi

log_info "–ù–∞—á–∏–Ω–∞–µ–º —É—Å—Ç–∞–Ω–æ–≤–∫—É –∏ –Ω–∞—Å—Ç—Ä–æ–π–∫—É 3x-ui."

# --- 1. –û–±–Ω–æ–≤–ª–µ–Ω–∏–µ –ø–∞–∫–µ—Ç–æ–≤ –∏ —É—Å—Ç–∞–Ω–æ–≤–∫–∞ –Ω–µ–æ–±—Ö–æ–¥–∏–º—ã—Ö —É—Ç–∏–ª–∏—Ç ---
log_info "1/4: –û–±–Ω–æ–≤–ª—è–µ–º —Å–ø–∏—Å–æ–∫ –ø–∞–∫–µ—Ç–æ–≤ –∏ —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º curl –∏ git..."
apt update -y || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ apt update."; exit 1; }
apt install -y curl git || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ curl –∏ git."; exit 1; }
log_success "–ü–∞–∫–µ—Ç—ã –æ–±–Ω–æ–≤–ª–µ–Ω—ã –∏ —É—Ç–∏–ª–∏—Ç—ã —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω—ã. ‚úÖ"

# --- 2. –°–∫–∞—á–∏–≤–∞–Ω–∏–µ –∏ —É—Å—Ç–∞–Ω–æ–≤–∫–∞ 3x-ui ---
log_info "2/4: –°–∫–∞—á–∏–≤–∞–µ–º –∏ —É—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º 3x-ui..."
# –ü—Ä–æ–≤–µ—Ä—è–µ–º, —Å—É—â–µ—Å—Ç–≤—É–µ—Ç –ª–∏ –ø–∞–ø–∫–∞ /opt/3x-ui
if [ -d "/opt/3x-ui" ]; then
    log_info "–î–∏—Ä–µ–∫—Ç–æ—Ä–∏—è /opt/3x-ui —É–∂–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç. –£–¥–∞–ª—è–µ–º –µ—ë –¥–ª—è —á–∏—Å—Ç–æ–π —É—Å—Ç–∞–Ω–æ–≤–∫–∏..."
    rm -rf /opt/3x-ui || { log_error "–ù–µ —É–¥–∞–ª–æ—Å—å —É–¥–∞–ª–∏—Ç—å —Å—É—â–µ—Å—Ç–≤—É—é—â—É—é –¥–∏—Ä–µ–∫—Ç–æ—Ä–∏—é /opt/3x-ui."; exit 1; }
fi

# –°–∫–∞—á–∏–≤–∞–µ–º —Å–∫—Ä–∏–ø—Ç —É—Å—Ç–∞–Ω–æ–≤–∫–∏ 3x-ui
curl -sS -L https://raw.githubusercontent.com/MHSanaei/3x-ui/master/install.sh -o /tmp/install_3xui.sh || { log_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–∫–∞—á–∞—Ç—å —É—Å—Ç–∞–Ω–æ–≤–æ—á–Ω—ã–π —Å–∫—Ä–∏–ø—Ç 3x-ui."; exit 1; }
chmod +x /tmp/install_3xui.sh || { log_error "–ù–µ —É–¥–∞–ª–æ—Å—å —Å–¥–µ–ª–∞—Ç—å —É—Å—Ç–∞–Ω–æ–≤–æ—á–Ω—ã–π —Å–∫—Ä–∏–ø—Ç –∏—Å–ø–æ–ª–Ω—è–µ–º—ã–º."; exit 1; }

# –ó–∞–ø—É—Å–∫–∞–µ–º —É—Å—Ç–∞–Ω–æ–≤–æ—á–Ω—ã–π —Å–∫—Ä–∏–ø—Ç 3x-ui
# –£–±—Ä–∞–ª–∏ —Ñ–ª–∞–≥ `-a`, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–∏–≤–æ–¥–∏–ª –∫ –æ—à–∏–±–∫–µ 404
/tmp/install_3xui.sh || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ –∑–∞–ø—É—Å–∫–µ —É—Å—Ç–∞–Ω–æ–≤–æ—á–Ω–æ–≥–æ —Å–∫—Ä–∏–ø—Ç–∞ 3x-ui."; exit 1; }
log_success "3x-ui —É—Å–ø–µ—à–Ω–æ —Å–∫–∞—á–∞–Ω –∏ —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω. ‚úÖ"

# --- 3. –ù–∞—Å—Ç—Ä–æ–π–∫–∞ 3x-ui: –∞–¥—Ä–µ—Å, –ø–æ—Ä—Ç –∏ —É—á–µ—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ ---
log_info "3/4: –ù–∞—Å—Ç—Ä–∞–∏–≤–∞–µ–º –∞–¥—Ä–µ—Å, –ø–æ—Ä—Ç –∏ —É—á–µ—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ 3x-ui..."

# –ö–æ–Ω—Ñ–∏–≥—É—Ä–∞—Ü–∏–æ–Ω–Ω—ã–π —Ñ–∞–π–ª 3x-ui (–ø–æ —É–º–æ–ª—á–∞–Ω–∏—é sqlite3 db)
THREEXUI_DB="/etc/x-ui/x-ui.db"

# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º –∞–¥—Ä–µ—Å –∏ –ø–æ—Ä—Ç
# –ò—Å–ø–æ–ª—å–∑—É–µ–º sqlite3 –¥–ª—è –º–æ–¥–∏—Ñ–∏–∫–∞—Ü–∏–∏ –±–∞–∑—ã –¥–∞–Ω–Ω—ã—Ö 3x-ui
sqlite3 "$THREEXUI_DB" "UPDATE settings SET value = '$THREEXUI_LISTEN_ADDRESS' WHERE key = 'WEB_LISTEN_IP';" || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ WEB_LISTEN_IP."; exit 1; }
sqlite3 "$THREEXUI_DB" "UPDATE settings SET value = '$THREEXUI_LISTEN_PORT' WHERE key = 'WEB_PORT';" || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ —É—Å—Ç–∞–Ω–æ–≤–∫–µ WEB_PORT."; exit 1; }
log_success "–ê–¥—Ä–µ—Å 3x-ui —É—Å—Ç–∞–Ω–æ–≤–ª–µ–Ω –Ω–∞ $THREEXUI_LISTEN_ADDRESS:$THREEXUI_LISTEN_PORT. üåê"

# –£—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —É—á–µ—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ root
# –°–Ω–∞—á–∞–ª–∞ —É–¥–∞–ª—è–µ–º –≤—Å–µ—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π, –∑–∞—Ç–µ–º –¥–æ–±–∞–≤–ª—è–µ–º 'root' —Å –Ω–æ–≤—ã–º –ø–∞—Ä–æ–ª–µ–º
# –≠—Ç–æ –≥–∞—Ä–∞–Ω—Ç–∏—Ä—É–µ—Ç, —á—Ç–æ —Å—Ç–∞—Ä—ã–µ —É—á–µ—Ç–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –Ω–µ –æ—Å—Ç–∞–Ω—É—Ç—Å—è.
sqlite3 "$THREEXUI_DB" "DELETE FROM users;" || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ —É–¥–∞–ª–µ–Ω–∏–∏ —Å—Ç–∞—Ä—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π."; exit 1; }
# –•–µ—à–∏—Ä–æ–≤–∞–Ω–∏–µ –ø–∞—Ä–æ–ª—è MD5, –∫–∞–∫ —ç—Ç–æ –¥–µ–ª–∞–µ—Ç 3x-ui
MD5_HASH=$(echo -n "$THREEXUI_ROOT_PASSWORD" | md5sum | awk '{print $1}')
sqlite3 "$THREEXUI_DB" "INSERT INTO users (username, password, enable) VALUES ('root', '$MD5_HASH', 1);" || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ –¥–æ–±–∞–≤–ª–µ–Ω–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è 'root'."; exit 1; }
log_success "–ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å 3x-ui 'root' —Å –Ω–æ–≤—ã–º –ø–∞—Ä–æ–ª–µ–º —É—Å–ø–µ—à–Ω–æ –¥–æ–±–∞–≤–ª–µ–Ω. üîë"

# --- 4. –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫ 3x-ui —Å–µ—Ä–≤–∏—Å–∞ ---
log_info "4/4: –ü–µ—Ä–µ–∑–∞–ø—É—Å–∫–∞–µ–º 3x-ui —Å–µ—Ä–≤–∏—Å –¥–ª—è –ø—Ä–∏–º–µ–Ω–µ–Ω–∏—è –∏–∑–º–µ–Ω–µ–Ω–∏–π..."
systemctl restart x-ui || { log_error "–û—à–∏–±–∫–∞ –ø—Ä–∏ –ø–µ—Ä–µ–∑–∞–ø—É—Å–∫–µ 3x-ui —Å–µ—Ä–≤–∏—Å–∞."; exit 1; }
log_success "3x-ui —Å–µ—Ä–≤–∏—Å —É—Å–ø–µ—à–Ω–æ –ø–µ—Ä–µ–∑–∞–ø—É—â–µ–Ω. ‚úÖ"

log_info "–£—Å—Ç–∞–Ω–æ–≤–∫–∞ –∏ –Ω–∞—Å—Ç—Ä–æ–π–∫–∞ 3x-ui –∑–∞–≤–µ—Ä—à–µ–Ω—ã."
log_info "3x-ui —Ç–µ–ø–µ—Ä—å –¥–æ—Å—Ç—É–ø–µ–Ω –ø–æ –∞–¥—Ä–µ—Å—É: http://$THREEXUI_LISTEN_ADDRESS:$THREEXUI_LISTEN_PORT"
log_info "–õ–æ–≥–∏–Ω: root, –ü–∞—Ä–æ–ª—å: <–ü–ê–†–û–õ–¨_–ö–û–¢–û–†–´–ô_–í–´_–ü–ï–†–ï–î–ê–õ–ò>"
log_info "–ï—Å–ª–∏ –≤—ã –Ω–µ –º–æ–∂–µ—Ç–µ –ø–æ–ª—É—á–∏—Ç—å –¥–æ—Å—Ç—É–ø, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ —Ñ–∞–π—Ä–≤–æ–ª–∞ (ufw –∏–ª–∏ nftables)."

log_info "–°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à–∏–ª —Ä–∞–±–æ—Ç—É."