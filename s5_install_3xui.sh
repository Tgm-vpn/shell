log_info "–õ–æ–≥–∏–Ω: $THREEXUI_USERNAME, –ü–∞—Ä–æ–ª—å: <–ü–ê–†–û–õ–¨_–ö–û–¢–û–†–´–ô_–í–´_–ü–ï–†–ï–î–ê–õ–ò>"
log_info "–ï—Å–ª–∏ –≤—ã –Ω–µ –º–æ–∂–µ—Ç–µ –ø–æ–ª—É—á–∏—Ç—å –¥–æ—Å—Ç—É–ø, –ø—Ä–æ–≤–µ—Ä—å—Ç–µ –Ω–∞—Å—Ç—Ä–æ–π–∫–∏ —Ñ–∞–π—Ä–≤–æ–ª–∞ (ufw –∏–ª–∏ nftables)."

log_info "–°–∫—Ä–∏–ø—Ç –∑–∞–≤–µ—Ä—à–∏–ª —Ä–∞–±–æ—Ç—É."