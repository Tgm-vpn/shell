# –ü—Ä–µ–¥–ø–æ–ª–∞–≥–∞–µ—Ç—Å—è, —á—Ç–æ —Å–∫—Ä–∏–ø—Ç –∑–∞–ø—É—Å–∫–∞–µ—Ç—Å—è –æ—Ç –∏–º–µ–Ω–∏ root.
# –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω–∏–µ: ./install_3xui.sh <–ü–ê–†–û–õ–¨_–î–õ–Ø_3XUI_ROOT>

# --- –ü–µ—Ä–µ–º–µ–Ω–Ω—ã–µ ---
